/**
 * Config Transaction Sync Dropdown Renderer
 * Handle state based on presence of transaction sync auth
 */
class Taxjar_SalesTax_Block_Adminhtml_Transactions extends Mage_Adminhtml_Block_System_Config_Form_Field
{
    protected function _getElementHtml(Varien_Data_Form_Element_Abstract $element)
    {
        $isAuthorized = Mage::getStoreConfig('tax/taxjar/transaction_auth');

        if (!$isAuthorized) {
            $element->setDisabled('disabled');
        }

        return parent::_getElementHtml($element);
    }
}
